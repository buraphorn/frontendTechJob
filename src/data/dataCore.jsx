// เครื่องมือ 3: ค้นหาพนักงานจากรหัสประจำตัว (ID)
export const getEmployeeById = (id) => employees.find(e => e.id === id);

// -----------------------------------------------------------
// ฟังก์ชันพิเศษ: ดึงประวัติพนักงานพร้อมประวัติงาน
// (ใช้สำหรับหน้า ManagerAccount เพื่อโชว์ว่าคนนี้เคยทำอะไรมาบ้าง)
// -----------------------------------------------------------
export const getEmployeeWithHistory = (id) => {
    // 1. ไปค้นข้อมูลส่วนตัวพนักงานมาก่อน
    const emp = getEmployeeById(id);
    if (!emp) return null; // ถ้าไม่เจอ ให้จบการทำงาน

    // 2. ไปค้นในกอง "ใบงาน (workTickets)" ว่าใบไหนบ้างที่ช่างคนนี้ (assigneeId) เป็นคนรับผิดชอบ
    const employeeJobs = workTickets.filter(ticket => ticket.assigneeId === emp.id);

    // 3. จัดระเบียบข้อมูลงานใหม่ ให้แสดงผลสวยๆ (เลือกเฉพาะ ชื่องาน, สถานที่, วันที่, สถานะ)
    const history = employeeJobs.map(job => ({
        id: job.id,
        jobTitle: job.namework || job.title,
        role: job.typework || "ช่างปฏิบัติงาน",
        location: job.location?.address || "ไม่ระบุ",
        date: job.datework,
        status: job.status
    }));

    // 4. ส่งข้อมูลพนักงาน + ประวัติงานที่รวบรวมได้ กลับออกไป
    return { ...emp, jobHistory: history };
};